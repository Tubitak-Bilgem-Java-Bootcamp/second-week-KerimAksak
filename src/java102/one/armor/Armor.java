package java102.one.armor;

public abstract class Armor{
    private int id;
    private int block;
    private String name;
    private int price;

    public Armor(int id, int block, String name, int price) {
        this.id = id;
        this.block = block;
        this.name = name;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getBlock() {
        return block;
    }

    public void setBlock(int block) {
        this.block = block;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
